package com.nytimes.news;

import android.app.Activity;
import android.app.Application;

import com.nytimes.news.di.components.DaggerAppComponent;

import javax.inject.Inject;

import dagger.android.AndroidInjector;
import dagger.android.DispatchingAndroidInjector;
import dagger.android.HasActivityInjector;

public class NewsTimeApp extends Application implements HasActivityInjector {

    private static NewsTimeApp sInstance;


    public static NewsTimeApp getAppContext() {
        return sInstance;
    }



    private static synchronized void setInstance(NewsTimeApp app) {
        sInstance = app;
    }
    @Inject
    DispatchingAndroidInjector<Activity> activityDispatchingInjector;

    @Override
    public void onCreate() {
        super.onCreate();
        initializeComponent();
        setInstance(this);
    }

    private void initializeComponent() {
        DaggerAppComponent.builder()
                .application(this)
                .build()
                .inject(this);
    }

    @Override
    public AndroidInjector<Activity> activityInjector() {
        return activityDispatchingInjector;
    }
}
